    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jtaMensagens.getModel();
        modelo.setNumRows(0);
        CorreioDAO pdao = new CorreioDAO();
        int i = 0;
        
        for (Correio p : pdao.read()) {
            i += 1;
            modelo.addRow(new Object[]{
                //p.getId_mensagem(),
                p.getRemetente(),
                p.getDestinatario(),
                p.getAssunto(),
                p.getMensagem(),
                p.getData(),
            });

        }

    }
    

    
    
